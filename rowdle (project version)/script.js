let index = Math.floor(Math.random(0) * 22);
let attempt = 1;
let won = false;

function submit() {
  if (attempt < 5 && !won) {
    answer = rowers[index];
    guess = rowers[Number(document.getElementById("guess").value)];
    document.querySelector(`.guess-${attempt}`).style.display = "grid";
    document.querySelector(`.guess-${attempt}>.box-1`).innerText = guess.name;
    document.querySelector(`.guess-${attempt}>.box-2`).innerText = guess.team;
    document.querySelector(`.guess-${attempt}>.box-3`).innerText = guess.boat;
    document.querySelector(`.guess-${attempt}>.box-4`).innerText = guess.side;
    document.querySelector(`.guess-${attempt}>.box-5`).innerText = guess.seat;
    document.querySelector(`.guess-${attempt}>.box-6`).innerText = guess.grade;
    document.querySelector(`.guess-${attempt}>.box-7`).innerText = guess.height;

    if (guess.name == answer.name) {
      document.querySelector(`.guess-${attempt}>.box-1`).style.background = "green";
      won = true;
    }
    if (guess.team == answer.team) {
      document.querySelector(`.guess-${attempt}>.box-2`).style.background = "green";
    }
    if (guess.boat == answer.boat) {
      document.querySelector(`.guess-${attempt}>.box-3`).style.background = "green";
    }
    if (guess.side == answer.side) {
      document.querySelector(`.guess-${attempt}>.box-4`).style.background = "green";
    }
    if (guess.seat == answer.seat) {
      document.querySelector(`.guess-${attempt}>.box-5`).style.background = "green";
    } else if (guess.seat == answer.seat + 1) {
      document.querySelector(`.guess-${attempt}>.box-5`).style.background = "yellow";
      document.querySelector(`.guess-${attempt}>.box-5`).innerText = guess.seat + " ↓";
    } else if (guess.seat == answer.seat - 1) {
      document.querySelector(`.guess-${attempt}>.box-5`).style.background = "yellow";
      document.querySelector(`.guess-${attempt}>.box-5`).innerText = guess.seat + " ↑";
    } else if (guess.seat > answer.seat) {
      document.querySelector(`.guess-${attempt}>.box-5`).innerText = guess.seat + " ↓";
    } else if (guess.seat < answer.seat) {
      document.querySelector(`.guess-${attempt}>.box-5`).innerText = guess.seat + " ↑";
    }
    if (answer.seat == "Cox") {
      document.querySelector(`.guess-${attempt}>.box-5`).innerText = guess.seat + " ↑";
    }
    if (guess.grade == answer.grade) {
      document.querySelector(`.guess-${attempt}>.box-6`).style.background = "green";
    } else if (guess.grade == answer.grade + 1) {
      document.querySelector(`.guess-${attempt}>.box-6`).style.background = "yellow";
      document.querySelector(`.guess-${attempt}>.box-6`).innerText = guess.grade + " ↓";
    } else if (guess.grade == answer.grade - 1) {
      document.querySelector(`.guess-${attempt}>.box-6`).style.background = "yellow";
      document.querySelector(`.guess-${attempt}>.box-6`).innerText = guess.grade + " ↑";
    } else if (guess.grade > answer.grade) {
      document.querySelector(`.guess-${attempt}>.box-6`).innerText = guess.grade + " ↓";
    } else if (guess.grade < answer.grade) {
      document.querySelector(`.guess-${attempt}>.box-6`).innerText = guess.grade + " ↑";
    }
    if (guess.height == answer.height) {
      document.querySelector(`.guess-${attempt}>.box-7`).style.background = "green";
    } else if (guess.heightInInches == answer.heightInInches + 1) {
      document.querySelector(`.guess-${attempt}>.box-7`).style.background = "yellow";
      document.querySelector(`.guess-${attempt}>.box-7`).innerText = guess.height + " ↓";
    } else if (guess.heightInInches == answer.heightInInches - 1) {
      document.querySelector(`.guess-${attempt}>.box-7`).style.background = "yellow";
      document.querySelector(`.guess-${attempt}>.box-7`).innerText = guess.height + " ↑";
    } else if (guess.heightInInches > answer.heightInInches) {
      document.querySelector(`.guess-${attempt}>.box-7`).innerText = guess.height + " ↓";
    } else if (guess.heightInInches < answer.heightInInches) {
      document.querySelector(`.guess-${attempt}>.box-7`).innerText = guess.height + " ↑";
    }

    attempt++;
  }
  document.getElementById("guess").value = "select";
  if (won) {
    document.querySelector(`.win-screen`).style.display = "inherit";
    document.querySelector(`.person`).innerText = answer.name;
  } else if (attempt == 5) {
    document.querySelector(`.lose-screen`).style.display = "inherit";
    document.querySelector(`.correct-person`).innerText = answer.name;
  }
}

function exit() {
  document.querySelector(".lose-screen").style.display = "none";
  document.querySelector(".win-screen").style.display = "none";
  document.querySelector(".manual-screen").style.display = "none";
}

function manual() {
  document.querySelector(".manual-screen").style.display = "inherit";
}
